const assert = require('assert');
const fs = require('fs');
const vm = require('vm');

const html = fs.readFileSync('hid_ui.html', 'utf8');
const scriptMatch = html.match(/<script>([\s\S]*)<\/script>/);
assert(scriptMatch, 'hid_ui.html must contain an inline script');

const parserSource = scriptMatch[1].slice(
  0,
  scriptMatch[1].indexOf('// ---------- rendering ----------'),
);
const context = {};
vm.createContext(context);
vm.runInContext(
  `${parserSource}\nglobalThis.hidTestApi = { parseDescriptor, SAMPLE };`,
  context,
);

const normalize = (value) => JSON.parse(JSON.stringify(value));
const parse = (descriptor) => normalize(context.hidTestApi.parseDescriptor(descriptor));
const rows = (descriptor) => parse(descriptor).groups.flatMap((group) => group.rows);

function expectError(descriptor, pattern) {
  assert.throws(() => context.hidTestApi.parseDescriptor(descriptor), pattern);
}

const sample = normalize(context.hidTestApi.parseDescriptor(context.hidTestApi.SAMPLE));
assert.strictEqual(sample.groups.length, 6);
assert.strictEqual(sample.groups.reduce((total, group) => total + group.rows.length, 0), 24);

const unsignedMaximum = rows(`
  0x05,0x01, 0x09,0x02, 0xA1,0x01,
  0x15,0x00, 0x25,0xFF, 0x75,0x08, 0x95,0x01,
  0x09,0x30, 0x81,0x02, 0xC0
`)[0];
assert.strictEqual(unsignedMaximum.logicalMax, 255);

const signedMaximum = rows(`
  0x05,0x01, 0x09,0x02, 0xA1,0x01,
  0x15,0x81, 0x25,0x7F, 0x75,0x08, 0x95,0x01,
  0x09,0x30, 0x81,0x02, 0xC0
`)[0];
assert.strictEqual(signedMaximum.logicalMin, -127);
assert.strictEqual(signedMaximum.logicalMax, 127);

const defaultPhysical = rows(`
  0x05,0x01, 0x09,0x02, 0xA1,0x01,
  0x15,0x01, 0x25,0x0A, 0x75,0x08, 0x95,0x01,
  0x09,0x30, 0x81,0x02, 0xC0
`)[0];
assert.deepStrictEqual([defaultPhysical.physicalMin, defaultPhysical.physicalMax], [1, 10]);

const zeroPhysical = rows(`
  0x05,0x01, 0x09,0x02, 0xA1,0x01,
  0x15,0x01, 0x25,0x0A, 0x35,0x00, 0x45,0x00,
  0x75,0x08, 0x95,0x01, 0x09,0x30, 0x81,0x02, 0xC0
`)[0];
assert.deepStrictEqual([zeroPhysical.physicalMin, zeroPhysical.physicalMax], [1, 10]);

const mixedUsageRows = rows(`
  0x05,0x01, 0x09,0x02, 0xA1,0x01,
  0x75,0x01, 0x95,0x03,
  0x09,0x30, 0x19,0x31, 0x29,0x32,
  0x81,0x02, 0xC0
`);
assert.deepStrictEqual(mixedUsageRows.map((row) => row.usageId), [0x30, 0x31, 0x32]);

const discreteArray = rows(`
  0x05,0x01, 0x09,0x02, 0xA1,0x01,
  0x75,0x08, 0x95,0x02,
  0x09,0x30, 0x09,0x32,
  0x81,0x00, 0xC0
`)[0];
assert.strictEqual(discreteArray.count, 2);
assert(discreteArray.usageText.includes('0x30'));
assert(discreteArray.usageText.includes('0x32'));
assert(!discreteArray.usageText.includes('0x30\u20130x32'));

const preferredDelimiterRows = rows(`
  0x05,0x01, 0x75,0x08, 0x95,0x02,
  0x09,0x30,
  0xA9,0x01, 0x09,0x31, 0xA9,0x00,
  0xA9,0x01, 0x09,0x32, 0xA9,0x00,
  0x81,0x02
`);
assert.deepStrictEqual(preferredDelimiterRows.map((row) => row.usageId), [0x30, 0x31]);

const metadata = rows(`
  0x05,0x01, 0x75,0x08, 0x95,0x01,
  0x09,0x30, 0x39,0x05, 0x79,0x07,
  0x81,0x02
`)[0];
assert.strictEqual(metadata.designatorLabel, '5');
assert.strictEqual(metadata.stringLabel, '7');

const completeFlags = rows(`
  0x05,0x01, 0x75,0x08, 0x95,0x01, 0x09,0x30,
  0x82,0x7E,0x01
`)[0].type;
for (const label of ['Variable', 'Relative', 'Wrap', 'Nonlinear', 'No Preferred', 'Null State', 'Buffered Bytes']) {
  assert(completeFlags.includes(label), `missing Main flag label: ${label}`);
}

const vendorCollection = parse(`0x05,0x01, 0x09,0x02, 0xA1,0x80, 0xC0`);
assert.strictEqual(vendorCollection.root[0].ctype, 'Vendor-defined (0x80)');

const splitItem = rows(`
  0x05,0x01,
  0x09,
  0x30,
  0x75,0x08, 0x95,0x01,
  0x81,
  0x02
`)[0];
assert.deepStrictEqual(splitItem.lines, [3, 4, 6, 7]);

expectError(`0x85,0x00`, /Report ID must be between 1 and 255/);
expectError(`0x75,0x01, 0x95,0x01, 0x81,0x02, 0x85,0x01`, /before the first data Main item/);
expectError(`0xB4`, /Pop has no matching Push/);
expectError(`0x05,0x01, 0x09,0x02, 0xA1,0x01`, /Collection item.*not closed/);
expectError(`0x75`, /Truncated 1-byte short item/);
expectError(`0xA9,0x01, 0x09,0x30, 0xA9,0x00, 0x75,0x08, 0x95,0x01, 0x81,0x00`, /Array item/);
expectError(`0x75,0x01, 0x95,0x01, 0x82,0x02,0x01`, /byte-aligned/);
expectError(`0x75,0x01, 0x95,0x01, 0x82,0x02,0x02`, /reserved flag bits/);

console.log('hid_ui HID 1.11 compliance tests passed');
